#include "../includes/cub3d.h"

int	__isdigit(int nb)
{
	if ((nb <= 57 && nb >= 48))
		return (1);
	return (0);
}

int	__isalnum(int k)
{
	if ((k <= 'z' && k >= 'a') || (k <= 'Z' && k >= 'A') 
		|| (k <= 57 && k >= 48))
		return (1);
	return (0);
}
